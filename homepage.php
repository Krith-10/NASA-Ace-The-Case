<h3 style="color: black">Earth’s Ocean</h3>
<p>Viewed from space, Earth appears as a blue marble, as approximately
70% of Earth’s surface is covered by ocean water. The vast ocean holds
roughly 97% of the planet’s water and represents 99% of the living space
on Earth. Below the surface, the ocean is teeming with life; in fact, most of
our planet’s lifeforms live in the ocean, from tiny microscopic organisms to
the enormous blue whale—the largest known animal on Earth.The world is getting warmer. Most of this warming has occurred since the
1970s, with the 20 warmest years having occurred since 1981 and with
all 10 of the warmest years occurring in the past 12 years. The ocean has
absorbed much of this increased heat, with the top 700 meters (about
2,300 feet) of ocean showing a warming of 0.302 degrees Fahrenheit since
1969. With global average sea surface temperatures on the rise, scientists
have also noticed a decline in phytoplankton in many ocean regions.</p> 

