<h3 style="color: black">What is Biomass?</h3>
<p>Biomass is living or once-living biological material. Strictly speaking
then, all life is biomass. All plants and animals on Earth today will at
some point die and decay—ashes to ashes, dust to dust. In most cases, however, when we use the term in this brochure, we are referring to
vegetation—living or dead. </p>
<h3 style="color: black">Biomass Burning</h3>
<p>Biomass burning is the act of burning living, or once-living, biological
material. Formerly believed to occur mainly in the tropics, Earth-observing satellites have revealed a global distribution of biomass burning. Although huge fires, often visible from space, occur in the savanna
grasslands of Africa and South America and tropical forests in South
America and Southeast Asia, they can take place wherever vegetation
is abundant. Boreal forests for example—found at much higher latitudes—are major sources of biomass burning.
Biomass burning results from either: 1) natural processes, such as ignition
by lightning strikes; or 2) anthropogenic (i.e., human-induced) activity, such
as burning vegetation for agricultural land clearing and preparing existing
fields for planting. Although natural and anthropogenic biomass burning
can occur almost anywhere on the globe, the majority of biomass burning in the tropics stems from human causes, whereas in extra-tropical
regions—such as boreal forests—natural causes are more common.</p>

