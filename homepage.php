<p>NASA is well known for observating space, but the agency also has a strong hand in monitoring Earth. NASA's Earth 
    Observing System (EOS) is a collection of satellites designed to monitor and understand key components of the climate system
    and their interactions over an extended period.<br>

"NASA and other space agencies had launched satellites to study [Earth] before," the agency said in a press release after EOS 
had operated for a decade and a half. "But the past 15 years have produced a more comprehensive look at Earth from space than any other period in history. At a time when our planet is undergoing critically important changes, this global
view offers not only stunning imagery but also vitally important information about how Earth is changing."</p>

