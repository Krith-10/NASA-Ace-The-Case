    <h3 style="color:black">WHAT IS THE ARCTIC?</h3>
<p>The Arctic is one of the coldest regions on Earth. Scientifically defined as
the region north of the Arctic Circle (located at 66° 33’ N latitude), the Arctic can be loosely defined by its icy waters largely bound by treeless land.
The extent of sea ice atop the cool, nutrient rich Arctic Ocean expands
and contracts in rhythm with the seasons, which oscillate between total
darkness and total daylight, year after year. Cooler temperatures and frozen soil limit the number of trees and other shrubs that grow in the Arctic;
in fact, our planet’s northernmost tree line currently hovers near the Arctic’s southern edge.For the past several decades, Earth-observing satellites have enabled continuous global observations of our home planet, and have played an especially important role in revealing the magnitude of change in remote, difficult
to access locations like the Arctic. Over the last decade, satellite data—
often combined with data from other sources—have revealed that the Arctic is changing at rates faster than anywhere else on Earth. These changes
include dramatic reductions in minimum sea-ice extent; alterations of landscape resulting from thawing of frozen soil, or permafrost; increases in phytoplankton productivity; changes in the extent and types of vegetation found
in the Arctic; and changes in the intensity of storms impacting Arctic coasts.
As temperatures rise in the Arctic, the extent of sea ice declines. Sea ice is
highly reflective of the Sun’s energy; therefore, reductions in sea ice impact
Earth’s radiation budget. Rather than reflecting most of the Sun’s energy,
ice-free areas absorb sunlight causing subsequent warming of the ocean.
Warmer ocean temperatures in turn impact Arctic coastlines, which can
cause permafrost to thaw and rates of erosion to accelerate. The warming
can also change global ocean and atmosphere circulations that subsequently affect weather patterns. Changes in the Arctic environment are
interrelated and have both long- and short-term effects on Earth’s land
surfaces, oceans, and atmosphere, ultimately impacting the Earth’s climate system. </p>

