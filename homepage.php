                <h2 style="font-family:Courier new;">UNDERSTANDING EARTH</h2>
                <b><h3 style="color:black; font-family: sans-serif">What is Biodiversity?</h3></b>
<p>Earth supports a diverse spectrum of life. Over billions of years, life has
evolved into a variety of forms—from microorganisms to our own species—
reflecting the rich array of environmental conditions present on our
changing planet.
Biological diversity, or biodiversity, refers to the variety of all life on Earth—
on all levels, from genes to species, ecosystems, and biomes. While it is
believed that Earth supports approximately 3 to 10 million species, only
about 1.5 million have been given a scientific name. Biodiversity is critical
for maintaining balanced ecosystems and provides a plethora of ecosystem
goods and services such as clean air, fresh water, waste removal, food,
fiber, and medicines.</p>

<img style="padding-left: 300px;"src="images/LPspin.gif" alt="Earth">
<h3 style="color: black">Using Earth Observations
    to Understand Biodiversity</h3>
<p>NASA is working in collaboration with experts from other federal agencies, universities, private companies, non-governmental organizations, and
international institutions to study patterns of biodiversity and understand
how biodiversity is changing. In particular, they are using both passive
and active satellite and airborne remote sensing technologies to directly
observe and identify biodiversity patterns, such as the distribution of
ecosystems and vegetation structure, as well as the environmental parameters that influence them such as topography and climate conditions.
Together, these observations strengthen our understanding of ecosystem
function and Earth’s biodiversity</p>

